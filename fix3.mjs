import { readFileSync, writeFileSync } from 'fs';
let c = readFileSync('app/nda/page.tsx', 'utf8');
c = c.replace(/Party's/g, "Party's");
c = c.replace(/Party\'s/g, "Party's");
c = c.replace(/it's/g, "it is");
c = c.replace(/Party's Confidential/g, "Party Confidential");
c = c.replace(/other Party's/g, "other Party");
c = c.replace(/receiving Party's/g, "receiving Party");
c = c.replace(/disclosing Party's/g, "disclosing Party");
writeFileSync('app/nda/page.tsx', c, 'utf8');
console.log('NDA apostrophes fixed!');